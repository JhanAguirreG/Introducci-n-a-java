public class main10 {
    public static void main(String[] args) {
        // Inicializar la variable de control del bucle
        int numero = 1;
        
        // Mientras el número sea menor o igual a 100, mostrarlo y luego incrementarlo
        while (numero <= 100) {
            System.out.println(numero);
            numero++; // Incrementar el número para pasar al siguiente
        }
    }
}
